# Escape Sequence:

print("Hello my name is Ali\nand i am from Pakistan.")
# \n for new line

# \' for single quote
print("It\'s a great day.") # output like: It's a great day.

# \"" for double quote
print("Hi, \'Imad\'")

# \\ for a backslash \
print("This is a backslash \\")

print("Hello\rWorld")